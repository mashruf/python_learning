# getting an element from an string
word = 'python'
print(word[1]) # y

# getting the last element of an string
word2 = "lmao"
print(word2[-1]) # o


# slicing
word3 = "Hello World"
print(word3[3:7]) # lo W, from 3 to 6 is printed

# or
print(word3[:7]) # Hello W, everything upto 7

# or 
print(word3[3:]) # lo World, from 3 to everything
